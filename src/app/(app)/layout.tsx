import type { Metadata } from "next";
import Image from "next/image";
import { Inter } from "next/font/google";
import "../globals.css";
import { getServerSession } from "next-auth";
import { authOption } from "../api/auth/[...nextauth]/route";
import SidebarNav from "@/components/layouts/SidebarNav";
import { connect } from "mongoose";
import Page from "@/models/Page.model.";
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";
import { faLink } from "@fortawesome/free-solid-svg-icons";
import { PageObject } from "@/../global";
import Link from "next/link";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Link Tree",
  description: "Generated by create next app",
};

export default async function AppLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  // @ts-ignore
  const session = await getServerSession(authOption);
  await connect(process.env.MONGODB_URI!);
  const page: PageObject | null = await Page.findOne({
    owner: session?.user?.email,
  });

  return (
    <html lang="en">
      <body className={inter.className}>
        <div className="flex">
          <aside className="h-screen max-h-screen w-48 flex-shrink-0 overflow-auto bg-white p-4 pt-8 shadow">
            <div className="relative mx-auto mb-8 h-[128px] w-[128px] overflow-hidden rounded-full">
              <Image
                src={session?.user?.image!}
                alt="profile-picture"
                fill
                className="h-full w-full object-cover object-center"
              />
            </div>

            {/* Public Page NavLink */}
            {page && (
              <Link
                target="_blank"
                href={`/${page.uri}`}
                className="my-4 flex items-center justify-center gap-1"
              >
                <FontAwesomeIcon
                  size="lg"
                  className="text-blue-500"
                  icon={faLink}
                />
                <span className="text-xl text-gray-300">/</span>
                <span>{page.uri}</span>
              </Link>
            )}

            <SidebarNav />
          </aside>

          <main className="max-h-screen flex-grow overflow-auto">
            {children}
          </main>
        </div>
      </body>
    </html>
  );
}
